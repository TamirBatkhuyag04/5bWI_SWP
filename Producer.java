package at.tamir.Camera;

public class Producer {
    // Instance variables
    private String name;
    private String country;

    // Constructor
    public Producer(String name, String country) {
        this.name = name;
        this.country = country;
    }

    // Getter
    public String getName() {
        return name;
    }

    public String getCountry() {
        return country;
    }

}
